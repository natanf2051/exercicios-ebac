package projeto;

public class dados {
    private String nome;
    private int idade;
    private String cidade;

    //usado para exibir
    public String getNome() {
        return nome;

    };
    //usado para receber os dados
    public void setNome(String nome){
        this.nome = nome;
    
    };
    //usado para exibir
    public int getIdade(){
        return idade;

    };
    //usado para receber os dados
    public void setIdade(int idade){
        this.idade = idade;
    
    };
    //usado para exibir
    public String getCidade(){
        return cidade;

    };
    //usado para receber os dados
    public void setCidade(String cidade){
        this.cidade = cidade;
    
    };


}
